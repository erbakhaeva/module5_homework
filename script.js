const arr = [0, 3, 8, 7, 5, 22, 10, 0, 47, 'hfjfjj', null, 0]
let oddCounter = 0
let evenCounter = 0
let zeroCounter = 0

for (let i = 0; i < arr.length; i++) {
  if (arr[i] === 0) {
    zeroCounter += 1
  } else if (typeof(arr[i]) !== 'number') {
    console.log(`это не число: ${arr[i]}`)
  } else if (arr[i] % 2 === 0) {
    evenCounter += 1
  } else {
    oddCounter +=1
  }
}
console.log(`это количество - ${evenCounter} - четных чисел`)
console.log(`это количество - ${oddCounter} - нечетных чисел`)
console.log(`это количество - ${zeroCounter} - нулей`)



